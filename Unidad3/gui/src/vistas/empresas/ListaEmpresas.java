    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        estado = 'E';
        btnBorrar.setVisible(true);
        btnGuardar.setText("Actualizar");
        DefaultTableModel modelo = (DefaultTableModel) tblDatos.getModel();
        int indiceTabla = tblDatos.getSelectedRow();

        Object valor = modelo.getValueAt(indiceTabla, 0);

        Empresa empresaEditar
                = empresaServicio.getEmpresa(valor.toString()).get(0);

        txtDireccion.setText(empresaEditar.getDireccion());
        txtEmail.setText(empresaEditar.getEmail());
        txtId.setText(Integer.toString(empresaEditar.getId()));
        txtNombre.setText(empresaEditar.getNombre());
        txtTelefono.setText(empresaEditar.getTelefono());

        tabs.setSelectedIndex(1);
        tabs.setEnabledAt(1, true);

        tabs.setTitleAt(1, "Editar cliente");
    }//GEN-LAST:event_tblDatosMouseClicked


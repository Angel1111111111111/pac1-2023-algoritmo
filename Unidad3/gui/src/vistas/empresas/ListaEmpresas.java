    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Empresa empresa = new Empresa();
        empresa.setDireccion(txtDireccion.getText());
        empresa.setEmail(txtEmail.getText());
        empresa.setId(Integer.parseInt(txtId.getText()));
        empresa.setNombre(txtNombre.getText());
        empresa.setTelefono(txtTelefono.getText());
        
        switch (estado) {
            case 'C':
                empresaServicio.agregar(empresa);
                break;
            case 'E':
                empresaServicio.editar(empresa);
                break;
            default:
                throw new AssertionError();
        }
        goBack();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void goBack() {
        tabs.setSelectedIndex(0);
        tabs.setEnabledAt(1, false);
        tabs.setTitleAt(1, "Mantenimiento");
        txtBuscar.setText("");
        txtDireccion.setText("");
        txtEmail.setText("");
        txtId.setText("");
        txtNombre.setText("");
        txtTelefono.setText("");
        obtenerDatos();

    }

    private void obtenerDatos() {
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        modelo.setColumnIdentifiers(empresaServicio.colunmasNombres);

        Object[] fila = new Object[modelo.getColumnCount()];

        for (Empresa c : empresaServicio.getEmpresa(txtBuscar.getText())) {
            fila[0] = c.getId();
            fila[1] = c.getNombre();
            fila[2] = c.getDireccion();
            fila[3] = c.getTelefono();
            fila[4] = c.getEmail();
            System.out.println("Datos: " + c.getId());
            modelo.addRow(fila);
        }

        tblDatos.setModel(modelo);

    }


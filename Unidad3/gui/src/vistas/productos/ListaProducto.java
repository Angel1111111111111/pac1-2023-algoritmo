    private void tblDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDatosMouseClicked
        estado = 'E';
        btnBorrar.setVisible(true);
        btnGuardar.setText("Actualizar");
        DefaultTableModel modelo = (DefaultTableModel) tblDatos.getModel();
        int indiceTabla = tblDatos.getSelectedRow();

        Object valor = modelo.getValueAt(indiceTabla, 0);

        Producto productoEditar
                = productosServicio.getProductos(valor.toString()).get(0);

        txtStock.setText(Integer.toString(productoEditar.getStock()));
        txtPrecio.setText(Double.toString(productoEditar.getPrecio()));
        txtTotal.setText(Double.toString(productoEditar.getTotal()));
        txtId.setText(Integer.toString(productoEditar.getId()));
        txtNombre.setText(productoEditar.getNombre());
        txtIva.setText(Double.toString(productoEditar.getIva()));

        tabs.setSelectedIndex(1);
        tabs.setEnabledAt(1, true);

        tabs.setTitleAt(1, "Editar Producto");
    }//GEN-LAST:event_tblDatosMouseClicked


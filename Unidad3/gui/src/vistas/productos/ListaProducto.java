    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        Producto producto = new Producto();
        producto.setId(Integer.parseInt(txtId.getText()));
        producto.setNombre(txtNombre.getText());
        producto.setStock(Integer.parseInt(txtStock.getText()));
        producto.setPrecio(Integer.parseInt(txtPrecio.getText()));
        producto.setIva(Integer.parseInt(txtIva.getText()));
        
        switch (estado) {
            case 'C':
                productosServicio.add(producto);
                break;
            case 'E':
                productosServicio.editar(producto);
                break;
            default:
                throw new AssertionError();
        }
        goBack();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void goBack() {
        tabs.setSelectedIndex(0);
        tabs.setEnabledAt(1, false);
        tabs.setTitleAt(1, "Mantenimiento");
        txtBuscar.setText("");
        txtPrecio.setText("");
        txtStock.setText("");
        txtTotal.setText("");
        txtId.setText("");
        txtNombre.setText("");
        txtIva.setText("");
        obtenerDatos();

    }

    private void obtenerDatos() {
        DefaultTableModel modelo = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        modelo.setColumnIdentifiers(productosServicio.colunmasNombres);

        Object[] fila = new Object[modelo.getColumnCount()];

        for (Producto c : productosServicio.getProductos(txtBuscar.getText())) {
            fila[0] = c.getId();
            fila[1] = c.getNombre();
            fila[2] = c.getStock();
            fila[3] = c.getPrecio();
            fila[4] = c.getTotal();
            fila[5] = c.getIva();
            fila[6] = c.getPrecioVenta();
            
            modelo.addRow(fila);
        }

        tblDatos.setModel(modelo);

    }

